    // Attributes to translate
    const TRANSLATABLE_ATTRS = ['placeholder', 'alt', 'aria-label', 'title'];

    // Walk all text nodes and replace
    function translateDOM(fromDict) {
        const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
        const nodes = [];
        let n;
        while ((n = walker.nextNode())) nodes.push(n);

        nodes.forEach(node => {
            const txt = node.nodeValue.trim();
            if (!txt) return;
            // Skip script/style contents
            const parent = node.parentNode;
            if (!parent) return;
            const tag = parent.nodeName;
            if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT') return;

            if (fromDict[txt]) {
                node.nodeValue = node.nodeValue.replace(txt, fromDict[txt]);
            }
        });

        // Translate attributes
        document.querySelectorAll('*').forEach(el => {
            TRANSLATABLE_ATTRS.forEach(attr => {
                const val = el.getAttribute(attr);
                if (val && fromDict[val.trim()]) {
                    el.setAttribute(attr, fromDict[val.trim()]);
                }
            });
        });

        // data-thought attributes on AI buttons
        document.querySelectorAll('[data-thought]').forEach(el => {
            const val = el.getAttribute('data-thought');
            if (val && fromDict[val]) {
                el.setAttribute('data-thought', fromDict[val]);
            }
        });
    }

    function setLang(lang) {
        const current = document.documentElement.getAttribute('data-lang') || 'ru';
        if (current === lang) return;

        if (lang === 'ua') {
            translateDOM(dict);
        } else {
            translateDOM(reverseDict);
        }

        document.documentElement.setAttribute('data-lang', lang);
        document.documentElement.setAttribute('lang', pageMeta[lang].lang);
        document.title = pageMeta[lang].title;
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) metaDesc.setAttribute('content', pageMeta[lang].description);

        try { localStorage.setItem('site_lang', lang); } catch(e) {}

        // Update switcher UI
        document.querySelectorAll('.lang-option').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.lang === lang);
        });

        // Notify AI demo to re-read thought keys (if loaded)
        if (window.__aiDemoRefresh) window.__aiDemoRefresh();
    }

